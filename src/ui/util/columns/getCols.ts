export function getAllCols(): ColTemp[] {
	return Object.entries(cols).map(([name, col]): Col => {
		return {
			...col,
			title: col.title ?? name,
			key: name,
		};
	});
}

export default (
	keys: string[],
	overrides: Record<string, Partial<MetaCol>> = {},
): MetaCol[] => {
	return keys.flatMap(key => {
		const col: MetaCol = {
			title: key,
			...(cols[key] ?? legacyCols[key] ?? {}),
			...overrides[key],
			key: key,
		};

		return [col];
	});
};
